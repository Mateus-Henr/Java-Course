package tutorial;

public class Main
{
    // Static Variables
    // Use the keywords static.
    // Also known as static member variables.
    // Every instance of that class shares the same static variable.
    // If changes are made to that variable, all other instances will see the effect of the change.
    // Scanner is static since we can pass it to different methods and etc.

    // Instance Variables
    // Also known as fields or member variables.
    // Belong to an specific instance of a class.
    // Each instance has it's own copy of an instance variable. (The concept of various objects from the same class)
    // Every instance can have a different value (state).
    // Instance variables represent the state of an instance.
}
